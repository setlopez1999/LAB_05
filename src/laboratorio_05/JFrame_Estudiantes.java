    private void lblBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBuscarMouseClicked
        // TODO add your handling code here:
        //Aqui escribimos lo que pasara si cliekeamos
        //Primero cambiamos la imagen
        lblBuscar.setIcon(new javax.swing.ImageIcon(
                getClass().getResource("/IMG/btnBuscar.png")));
        
        //Ahora sacamos el texto del txtBuscar
        String stud_buscado = txtBuscar.getText();
        //Evaluamos si esta vacio
        if(stud_buscado.equals("")){
            //No hacemso nada
        }else{
            //Ahora buscamos en la lista
            //iteramos en cada posicion
            int pos=-1;
            for(int i = 0 ; i<listaEstudiantes.size(); i++){
                //Ahora en cada elemento estudiante
                Estudiante estud_actual = listaEstudiantes.get(i);
                //Vemos si se encuentra el stud_buscado
                if( estud_actual.getNombre().equals(stud_buscado)||
                    estud_actual.getApellido().equals(stud_buscado)||
                    estud_actual.getCodigo().equals(stud_buscado)){
                    pos = i;
                    break;
                }
            }
            //Mostramos el mensaje
            if(pos != -1){
                
                JOptionPane.showMessageDialog(null,""
                    + "El Dato fue encontrado en la fila "+ (pos+1));
            }
            else{
                JOptionPane.showMessageDialog(null,""
                    + "El Dato NO fue encontrado");
            }
            
            
        }
        
        
    }//GEN-LAST:event_lblBuscarMouseClicked

