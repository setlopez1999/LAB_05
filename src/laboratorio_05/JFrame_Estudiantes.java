    private void lblEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEliminarMouseClicked
        // TODO add your handling code here:
        //Buscamos el indice actual
        int index = BuscarIndiceActual();
        if(index == -1){
            //No hacemos nada
        }else{
            //Lo removemos del modelo
            modelo.remove(index);
            //Lo removemos de la lista
            listaEstudiantes.remove(index);
            //Ahora actualizamos el modelo de la interfaz
            ActualizarModelo(modelo);
    
        }
    }//GEN-LAST:event_lblEliminarMouseClicked
    
